    private void importarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_importarMouseClicked
        try {
            diccionario = new Diccionario(textARespuesta);
            if (diccionario != null) {
                cantidad.setText(diccionario.getCantidadp() + "");
            }
        } catch (IOException ex) {
            textARespuesta.setText(ex.getLocalizedMessage());
        } catch (Exception ex) {
            Logger.getLogger(JFPractica2.class.getName()).log(Level.SEVERE, null, ex);
        }
        importar.setEnabled(false);
    }//GEN-LAST:event_importarMouseClicked

