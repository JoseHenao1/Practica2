    private void completaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_completaActionPerformed
        NodoAVL aux;
        String palabra = DataP.getText();
        if (!palabra.equals("") && diccionario != null) {
            NodoAVL prueba = new NodoAVL(DataP.getText());
            if (diccionario.buscarComplejo(prueba.getDato(), 0) == null) {
                aux = diccionario.insertarPalabraS(DataP.getText());
                diccionario.InsertarPalabraC(aux);
            } else if (prueba.getSignificado() == null || prueba.getSinonimo() == null || prueba.getAntonimo() == null) {
                prueba = diccionario.buscarComplejo(prueba.getDato(), 0);
                diccionario.InsertarPalabraC(prueba);
            } else {
                JOptionPane.showMessageDialog(null, "Esta palabra ya existe");
            }
        } else {
            if (diccionario == null) {
                JOptionPane.showMessageDialog(null, "Error, recuerde importar los archivos");
            } else {
                JOptionPane.showMessageDialog(null, "Error, espacio en blanco no permitido");
            }

        }

        textPalabra.setText("");
    }//GEN-LAST:event_completaActionPerformed


    private void simpleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpleActionPerformed
        try {
            String palabra = DataP.getText();
            if (!palabra.equals("") && diccionario != null) {
                if (diccionario.buscarComplejo(DataP.getText(), 0) == null) {
                    diccionario.insertarPalabraS(DataP.getText());
                } else {
                    JOptionPane.showMessageDialog(null, "Esta palabra ya existe");
                }
            } else {
                if (diccionario == null) {
                    JOptionPane.showMessageDialog(null, "Error, recuerde importar los archivos");
                } else {
                    JOptionPane.showMessageDialog(null, "Error, espacio en blanco no permitido");
                }
            }
            textPalabra.setText("");

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Algo salio mal");
        }
    }//GEN-LAST:event_simpleActionPerformed

